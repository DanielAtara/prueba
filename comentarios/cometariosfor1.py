for i in range(11):#se ingresa el limite de ciclo for 
    if i%3==0:# se evaula que numeros son multiplos de 3 
        print(f'{i} es multiplo de 3')# se imprimen los numeros multiplos de 3 
    else:
        print(i)#se siguen imprimiendo los numeros hasta el limite anteriormente planteado 

for j in range(1,11): # se da un numero de punto de partida y uno de finalizacion 
    print(j) # se ejecuta lo anterimente nombrado 

for k in range(0,11,2): # se da un numero de punto de partida y uno de finalizacion y se incrementa de dos en dos 
    print(k)

for i in range(20,0,-2): # se da un numero de punto de partida y uno de finalizacion y se le da la asginacion que sean descontados los numeros de dos en dos 
    print(i)